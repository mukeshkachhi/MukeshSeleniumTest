package basicOfJava;

public class JavaVariable {

	public static void main(String[] args) 
	{
		// A Block in the java program. 
		
		int a =10; // size 4 byte
		char b ='M'; // contains only single character
		String c= "I am New"; // its a class and datatype. 
		float d = 10.25f; // java should know its a float so end with f
		double e = 10.2345678d; //java should know its a float so end with d
		boolean t = true; 
		
		
		System.out.println("Value of Integer " + a);
		System.out.println("Value of char " + b);
		System.out.println("Value of string " + c);
		System.out.println("Value of float " + d);
		System.out.println("Value of double " + e);
		System.out.println("Value of boolean " + t);
		
	}

}
